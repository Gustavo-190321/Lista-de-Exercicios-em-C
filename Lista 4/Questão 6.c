#include <stdio.h>
#include <stdlib.h>

/* run this program using the console pauser or add your own getch, system("pause") or input loop */

int main(int argc, char *argv[]) {
	int i, j, k;
	printf("Tabela 3x4:\n");
	for(i = 0; i < 3; i++){
		for(j = 0; j < 4; j++){
			printf(" - ", k);
			k++;
		}
		printf("\n");
	}
	return 0;
}
